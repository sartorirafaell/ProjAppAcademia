    private void jPasswordTestFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordTestFocusLost
        if(jPasswordTest.getText().length()==0){
            jPasswordTest.setText("Repita a nova senha");
            if(jPasswordTest.getText().equals("Repita a nova senha")){
            jPasswordTest.setEchoChar((char)0);
        }
            Font font = new Font("Arial", Font.PLAIN, 12);
            jPasswordTest.setFont(font);
            jPasswordTest.setForeground(Color.GRAY);
                    
        }  
        if( !jPasswordTest.getText().equals(jNewPassword.getText()) && !jPasswordTest.getText().equals("Repita a nova senha") ){
            jLabelErroSenha.setVisible(true);
        } else {
            jLabelErroSenha.setVisible(false);
        }
    }//GEN-LAST:event_jPasswordTestFocusLost

