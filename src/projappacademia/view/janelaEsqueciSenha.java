    private void jTextLoginFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextLoginFocusGained
        if(jTextLogin.getText().equals("Digite o login")){
            jTextLogin.setText(null);
            jTextLogin.requestFocus();
            Font font = new Font("Arial", Font.PLAIN, 12);
            jTextLogin.setFont(font);
            jTextLogin.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_jTextLoginFocusGained

    private void jTextLoginFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextLoginFocusLost
        if(jTextLogin.getText().length()==0){
            jTextLogin.setText("Digite o login");
            Font font = new Font("Arial", Font.PLAIN, 12);
            jTextLogin.setFont(font);
            jTextLogin.setForeground(Color.GRAY);
                    
        }
    }//GEN-LAST:event_jTextLoginFocusLost

    private void jNewPasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jNewPasswordFocusGained
        if(jNewPassword.getText().equals("Digite a nova senha")){
            jNewPassword.setText(null);
            jNewPassword.requestFocus();
            jNewPassword.setEchoChar('*');
            Font font = new Font("Arial", Font.PLAIN, 12);
            jNewPassword.setFont(font);
            jNewPassword.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_jNewPasswordFocusGained

    private void jNewPasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jNewPasswordFocusLost
        if(jNewPassword.getText().length()==0){
            jNewPassword.setText("Digite a nova senha");
            if(jNewPassword.getText().equals("Digite a nova senha")){
            jNewPassword.setEchoChar((char)0);
        }
            Font font = new Font("Arial", Font.PLAIN, 12);
            jNewPassword.setFont(font);
            jNewPassword.setForeground(Color.GRAY);
                    
        }
    }//GEN-LAST:event_jNewPasswordFocusLost

